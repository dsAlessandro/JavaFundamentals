package Points;

public class threeDimPoint {
    int x;
    int y;
    int z;

    public threeDimPoint() {
        x = 0;
        y = 0;
        z = 0;
    }

    public int getx() {
        return x;
    }

    public int gety() {
        return y;
    }
    public int getz() {
        return z;
    }



    public void setx(int v) {
        x = v;
    }

    public void sety(int v) {
        y = v;
    }

    public void setz(int v) {
        z = v;
    }
}
