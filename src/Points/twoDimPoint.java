package Points;

public class twoDimPoint {
    int x;
    int y;

    public twoDimPoint() {
        x = 0;
        y = 0;
    }

    public int getx() {
        return x;
    }

    public int gety() {
        return y;
    }


    public void setx(int v) {
        x = v;
    }

    public void sety(int v) {
        y = v;
    }
}
